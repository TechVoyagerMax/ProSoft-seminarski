    private void forInsert(){
        txtId.setVisible(false);
        jLabel1.setVisible(false);
    }
    
    private void popuniCombo(){
        Integer[] brojevi={5,6,7,8};
        for(Integer i:brojevi){
            comboPredmet.addItem(i);
        }
        comboPredmet.setSelectedItem(null);
    }
    
    private boolean vecUnet(){
        ArrayList<Razred> svi=Controller.getInstance().vratiListuSviRazred();
        Razred r=new Razred();
        r.setRedni_broj((Integer)comboPredmet.getSelectedItem());
        for(Razred razred:svi){
            if(razred.getRedni_broj()==r.getRedni_broj()){
                return true;
            }
        }
        return false;
    }

