    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        Razred razred=new Razred();
        if(vecUnet()==false){
            razred.setRedni_broj((Integer)comboPredmet.getSelectedItem());
            int odg=JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da sacuvate razred?","Potvrda",JOptionPane.YES_NO_OPTION);
            if(odg==0){
                razred.setId(Controller.getInstance().kreirajRazred(razred));
                if(razred.getId()!=0){
                    JOptionPane.showMessageDialog(this, "Uspesno sacuvan razred u bazi!");
                    this.setVisible(false);
                }else{
                    JOptionPane.showMessageDialog(this, "Greska pri ucitavanju","Greska",JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "Nece biti sacuvano");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Izabrani razred vec posotji u bazi!","Obavestenje",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

