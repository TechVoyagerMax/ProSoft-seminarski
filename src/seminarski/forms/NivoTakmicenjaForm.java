    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        NivoTakmicenja nivo=new NivoTakmicenja();
        if(ponovanUnos()==false){
            nivo.setNaziv((String)comboNivo.getSelectedItem());
            int odg=JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da sacuvate nivo?","Potvrda",JOptionPane.YES_NO_OPTION);
            if(odg==0){
                nivo.setId(Controller.getInstance().kreirajNivo(nivo));
                if(nivo.getId()!=0){
                    JOptionPane.showMessageDialog(this, "Uspesno sacuvano!");
                    this.setVisible(false);
                }else{
                    JOptionPane.showMessageDialog(this, "Greska prilikom cuvanja nivoa","Greska",JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "Nece biti sacuvano!");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Nivo vec postoji u bazi","Obavestenje",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        NivoTakmicenja stari_nivo=new NivoTakmicenja();
        stari_nivo.setId(Long.parseLong(txtId.getText()));
        stari_nivo.setNaziv((String)comboNivo.getSelectedItem());
        int odg=JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da obrisete nivo takmiceja?","Potvrda",JOptionPane.YES_NO_OPTION);
        if(odg==0){
            
            ArrayList<Takmicenje> takmicenje_brisanje=Controller.getInstance().vratiListuTakmicenja(stari_nivo);
            ArrayList<StavkaTakmicenja> stvake_brisanje=new ArrayList<StavkaTakmicenja>();
            for(Takmicenje t:takmicenje_brisanje){
                ArrayList<StavkaTakmicenja> li=Controller.getInstance().vratiListuStavkiTakmicenja(t);
                for(StavkaTakmicenja sss:li){
                    stvake_brisanje.add(sss);
                }
            }
            for(StavkaTakmicenja st:stvake_brisanje){
                if(Controller.getInstance().obrisiStavkuStavke(st)){
                }
            }
            for(Takmicenje t:takmicenje_brisanje){
                if(Controller.getInstance().obrisiStavkuTakmicenja(t)){
                }
            }
            if(Controller.getInstance().obrisiTakmicenje(stari_nivo)){
                if(Controller.getInstance().obrisiNivoTakmicenja(stari_nivo)){
                    JOptionPane.showMessageDialog(this, "Uspesno obrisano");
                    this.setVisible(false);
                    new PrikazNivoTakmicenjaForm(null, true).setVisible(true);
                }
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "Nece biti obrisnao");
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

