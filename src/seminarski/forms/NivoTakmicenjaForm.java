    private void popuniCombo(){
        String[] nivoi={"Skolsko takmicenje","Opstinsko takmicenje","Okruzno takmicenje","Republicko takmicenje"};
        for(String s:nivoi){
            comboNivo.addItem(s);
        }
        comboNivo.setSelectedItem(null);
    }
    
    private void forInsert(){
        txtId.setVisible(false);
        lblId.setVisible(false);
        btnObrisi.setVisible(false);
    }
    
    private boolean ponovanUnos(){
        ArrayList<NivoTakmicenja> svi=Controller.getInstance().vratiListuSviNivoiTakmicenja();
        NivoTakmicenja nivo=new NivoTakmicenja();
        nivo.setNaziv((String)comboNivo.getSelectedItem());
        for(NivoTakmicenja n:svi){
            if(n.getNaziv().equals(nivo.getNaziv())){
                return true;
            }
        }
        return false;
    }
    
    private void forView(NivoTakmicenja nt){
        btnSacuvaj.setVisible(false);
        jLabel2.setVisible(false);
        txtId.setText(nt.getId()+"");
        comboNivo.setSelectedItem(nt.getNaziv());
        txtId.setEnabled(false);
        comboNivo.setEnabled(false);
    }

