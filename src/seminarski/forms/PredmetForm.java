    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        Predmet predmet=new Predmet();
        if(novUnos()==false){
            predmet.setNaziv(txtNaziv.getText().trim());
            int odg=JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da sacuvate novi predmet?","Potvrdite",JOptionPane.YES_NO_OPTION);
            if(odg==0){
                predmet.setId(Controller.getInstance().kreirajPredmet(predmet));
                if(predmet.getId()!=0){
                    JOptionPane.showMessageDialog(this, "Uspesno sacuvano!");
                    this.setVisible(false);
                }else{
                    JOptionPane.showMessageDialog(this, "Greska prilikom cuvanja predmeta","Greska",JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "Nece biti sacuvano!");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Predmet vec postoji u bazi","Obavestenje",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        Predmet stariPredmet=new Predmet();
        stariPredmet.setId(Long.parseLong(txtId.getText()));
        stariPredmet.setNaziv(txtNaziv.getText());
        int odg=JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da obrisete predmet?","Provera",JOptionPane.YES_NO_OPTION);
        if(odg==0){
            ArrayList<Takmicenje> takmicenje_brisanje=Controller.getInstance().vratiListuTakmicenja(stariPredmet);
            ArrayList<StavkaTakmicenja> stvake_brisanje=new ArrayList<StavkaTakmicenja>();
            for(Takmicenje t:takmicenje_brisanje){
                ArrayList<StavkaTakmicenja> li=Controller.getInstance().vratiListuStavkiTakmicenja(t);
                for(StavkaTakmicenja sss:li){
                    stvake_brisanje.add(sss);
                }
            }
            
            for(Takmicenje tt:takmicenje_brisanje){
                if(Controller.getInstance().obrisiSala(tt)){
                }
            }
            
            for(StavkaTakmicenja st:stvake_brisanje){
                if(Controller.getInstance().obrisiStavkuStavke(st)){
                }
            }
            for(Takmicenje t:takmicenje_brisanje){
                if(Controller.getInstance().obrisiStavkuTakmicenja(t)){
                }
            }
            
            if(Controller.getInstance().obrisiTakmicenje(stariPredmet)){
                if(Controller.getInstance().obrisiPredmet(stariPredmet)){
                    JOptionPane.showMessageDialog(this, "Uspesno obrisano");
                    this.setVisible(false);
                    new PrikazPredmetaForm(null, true).setVisible(true);
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Nece biti obrisano");
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

