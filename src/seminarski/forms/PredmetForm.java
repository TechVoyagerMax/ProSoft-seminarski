    private void forInsert(){
        txtId.setVisible(false);
        lblId.setVisible(false);
        btnObrisi.setVisible(false);
        btnIzmeni.setVisible(false);
        btnPromeni.setVisible(false);
    }
    
    private void forView(Predmet p){
        btnSacuvaj.setVisible(false);
        jLabel2.setVisible(false);
        txtId.setText(p.getId()+"");
        txtNaziv.setText(p.getNaziv());
        txtId.setEnabled(false);
        txtNaziv.setEnabled(false);
        btnIzmeni.setVisible(false);
    }
    
    private boolean novUnos(){
        ArrayList<Predmet> svi=Controller.getInstance().vratiListuSviPredmet();
        Predmet p=new Predmet();
        p.setNaziv(txtNaziv.getText().trim());
        for(Predmet predmet:svi){
            if(predmet.getNaziv().equals(p.getNaziv())){
                return true;
            }
        }
        return false;
    }

